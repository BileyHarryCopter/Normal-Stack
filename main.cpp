#include "stack.h"

int main () {
    struct Stack stk = {};
    StackCtor (&stk);
//===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*//
//===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*//
    for (int i = 0; i < 10; i++)
    {
        StackPush (&stk, 10.2 + i);
    }
    for (int i = 0; i < 11; i++) {
        printf ("data[%d] = %lf\n", 9 - i, StackPop(&stk));
    }
//===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*//
//===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*//
    StackDtor (&stk);
    return 0;
}
